    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String name = txtUsername.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        String email = txtEmail.getText();
        String phoneNumb = txtPhoneNumber.getText();
        try {
            spnAge.commitEdit();
        } catch ( java.text.ParseException e ) { spnAge.setValue(0); }
        int age = (Integer) spnAge.getValue();
        if (Main.validator.isValidEmail(email) && Main.validator.isValidName(name) && Main.validator.isValidPassword(password) && Main.validator.isValidPhoneNumber(phoneNumb) && age>=1 && age <=150){
           admin.setAge(age);
           admin.setEmail(email);
           admin.setName(name);
           admin.setPassword(password);
           admin.setPhoneNumb(phoneNumb);
           admin.setUsername(username);
           diaSuccessfullyUpdated.pack();
           diaSuccessfullyUpdated.setVisible(true);
           diaSuccessfullyUpdated.getPreferredSize();
        } else {
           diaInvalidInput.pack();
           diaInvalidInput.setVisible(true);
           diaInvalidInput.getPreferredSize();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

